#include "pic.h"
#include "../io/io.h"


void init_pic(){
  outb(0x20,0x11);
  outb(0xA0,0x11);

  outb(0x21,0x20);
  outb(0xA1,0x28);

  outb(0x21,0x04);
  outb(0xA1,0x02);

  outb(0x21,0x05);
  outb(0xA1,0x01);

  outb(0x21,0xfd);
  outb(0xA1,0xff);

  asm("sti");
}

void pic_ack() {
  outb(0x20,0x20);
}

uint8_t read_keyboard_data(){
  return inb(0x60);
}